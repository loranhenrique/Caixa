package Estabelecimento2;

public class Produtos {

	private String scodigo, snome, stamanho;
	private float svalor;
	private int squantidade;
	private double sdesconto;
	
	
	public double getSdesconto() {
		return sdesconto;
	}
	public void setSdesconto(double sdesconto) {
		this.sdesconto = sdesconto;
	}
	public String getScodigo() {
		return scodigo;
	}
	public void setScodigo(String scodigo) {
		this.scodigo = scodigo;
	}
	public String getSnome() {
		return snome;
	}
	public void setSnome(String snome) {
		this.snome = snome;
	}
	public String getStamanho() {
		return stamanho;
	}
	public void setStamanho(String stamanho) {
		this.stamanho = stamanho;
	}
	public float getSvalor() {
		return svalor;
	}
	public void setSvalor(float svalor) {
		this.svalor = svalor;
	}
	public int getSquantidade() {
		return squantidade;
	}
	public void setSquantidade(int squantidade) {
		this.squantidade = squantidade;
	}

}
